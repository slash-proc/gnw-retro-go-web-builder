#!/usr/bin/env python3
"""Ingest the Game Genie xlsx into cheats.json for the web app. Stdlib only.
Re-run: python3 apps/web/src/lib/cheats/ingest.py"""
import zipfile, xml.etree.ElementTree as ET, re, json, os
NS='{http://schemas.openxmlformats.org/spreadsheetml/2006/main}'
RNS='{http://schemas.openxmlformats.org/officeDocument/2006/relationships}'
SRC="references/Game Genie Code Compilation - Shared.xlsx"
OUT="apps/web/src/lib/cheats/cheats.json"
SYS={'NES':'nes','SNES':'snes','Game Boy':'gb','Genesis':'genesis','Game Gear':'gamegear'}
CODE=re.compile(r'[A-Z0-9]+([ +\-]+[A-Z0-9]+)*')  # uppercase GG code(s); rejects note rows
z=zipfile.ZipFile(SRC)
ss=[''.join(t.text or '' for t in si.iter(f'{NS}t')) for si in ET.fromstring(z.read('xl/sharedStrings.xml')).findall(f'{NS}si')]
sheets=[(s.get('name'),s.get(f'{RNS}id')) for s in ET.fromstring(z.read('xl/workbook.xml')).find(f'{NS}sheets')]
rid2t={r.get('Id'):r.get('Target') for r in ET.fromstring(z.read('xl/_rels/workbook.xml.rels'))}
n2f={n:('xl/'+rid2t[r].lstrip('/')).replace('xl/xl/','xl/') for n,r in sheets if r in rid2t}
def rows(name):
    sd=ET.fromstring(z.read(n2f[name])).find(f'{NS}sheetData')
    for row in sd.findall(f'{NS}row'):
        d={}
        for c in row.findall(f'{NS}c'):
            v=c.find(f'{NS}v'); cl=re.match(r'[A-Z]+',c.get('r')).group()
            d[cl]=(ss[int(v.text)] if (v is not None and c.get('t')=='s') else (v.text if v is not None else '')).strip()
        yield d
def norm(t):
    t=re.sub(r'\([^)]*\)','',t).lower()
    t=re.sub(r'^(the|a)\s+','',t)
    return re.sub(r'[^a-z0-9]','',t)
db={}; counts={}; skipped={}
for disp,key in SYS.items():
    ccol=ecol=None; gcol='A'; games={}; skip=0
    for r in rows(disp):
        if ccol is None:                                   # find header row by "Key in…"
            ki=[cl for cl,t in r.items() if t.startswith('Key in')]
            if ki:
                ccol=ki[0]; ef=[cl for cl,t in r.items() if t.startswith('Effect')]; ecol=ef[0] if ef else None
            continue
        g=r.get(gcol,'').strip(); code=re.sub(r'\s+',' ',r.get(ccol,'').strip())
        eff=r.get(ecol,'').strip() if ecol else ''
        if not g or not code: continue
        if not CODE.fullmatch(code): skip+=1; continue     # drop note/free-text rows
        games.setdefault(g,[]).append({'code':code,'effect':eff})
    db[key]=[{'title':g,'key':norm(g),'cheats':c} for g,c in games.items()]
    counts[key]={'games':len(db[key]),'codes':sum(len(x['cheats']) for x in db[key])}; skipped[key]=skip
out={'meta':{'source':os.path.basename(SRC),'note':'Game Genie codes; generated by ingest.py','counts':counts},'systems':db}
json.dump(out,open(OUT,'w'),ensure_ascii=False,separators=(',',':'))
print('counts:',json.dumps(counts)); print('skipped(non-code rows):',json.dumps(skipped)); print('bytes:',os.path.getsize(OUT))
def find(s,k): return next((x for x in db[s] if x['key']==k),None)
for s,k in [('nes','supermariobros'),('gamegear','addamsfamily'),('snes','supermarioworld')]:
    g=find(s,k); print(s,k,'->',(g['title'],len(g['cheats']),g['cheats'][0]) if g else None)
